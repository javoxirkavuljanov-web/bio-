    private val translations = mapOf(
        AppLanguage.EN to en,
        AppLanguage.RU to ru,
        AppLanguage.UZ to uz
    )

    fun get(key: String, lang: String): String {
        val appLanguage = when (lang.lowercase()) {
            "en" -> AppLanguage.EN
            "uz" -> AppLanguage.UZ
            else -> AppLanguage.RU // Default to Russian as per prompt preference
        }
        return translations[appLanguage]?.get(key) ?: translations[AppLanguage.EN]?.get(key) ?: key
    }
